
public class Dongu1 {

	public static void main(String[] args) {
		System.out.println("For Döngüsü");
		for (int i = 0; i < 5; i++) {
			System.out.println(i);
		}
		System.out.println("While döngüsü");
		int j = 5;
		while (j < 5) {
			System.out.println(j);
			j++;
		} 
		System.out.println("do döngüsü");
		j=5;
		do {
			System.out.println(j);
			j++;
		} while (j < 5);
		System.out.println("Program sonu");
	}
}
