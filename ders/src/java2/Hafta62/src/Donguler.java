public class Donguler {
	public static void main(String[] args) {
		System.out.println("For döngüsü");
		for (int i = 0; i < 3; i++) {
			System.out.println(i);
		}
		int i = 0;
		System.out.println("While döngüsü");
		while(i < 3) {
			System.out.println(i);
			i++;
		}
		System.out.println("Do döngüsü");
		i=0;
		do {
			System.out.println(i); 
			i++;
		}while(i < 3);
		System.out.println("PRogram bitti");
	}

}
