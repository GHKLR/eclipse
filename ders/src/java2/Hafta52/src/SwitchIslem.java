
public class SwitchIslem {

	public static void main(String[] args) {
		int sayi = 5;
		switch (sayi) {
		case 1:
			System.out.println("1 girdin");break;
		case 3:
			System.out.println("3 girdin");
			break;
		case 6:
			System.out.println("6 girdin");
		case 7:
			System.out.println("7 girdin");
			break;
		default:
			System.out.println("1,3,6,7 dışında girdin");
			break;
		}

	}

}
